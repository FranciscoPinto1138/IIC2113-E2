namespace Fire_Emblem;

public class BonusNeutralizationManager
{
    public int Atk;
    public int Spd;
    public int Res;
    public int Def;
    public int HP;
    
    public BonusNeutralizationManager()
    {
        this.Atk = 1;
        this.Spd = 1;
        this.Res = 1;
        this.Def = 1;
        this.HP = 1;
    }
}